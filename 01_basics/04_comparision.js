// Normal Comparision output in true or false
console.log(2 > 1)
console.log(2 >= 1)
console.log(2 < 1)
console.log(2 == 1)
console.log(2 != 1)

// two different datatype comparision output in true or false
console.log("2" > 1)
console.log("02" > 1)

// comparision with null output in true or false
console.log(null > 0)
console.log(null == 0)
console.log(null >= 0)

// compare with undefined output always false
console.log(undefined == 0)
console.log(undefined > 0)
console.log(undefined < 0)

// == compare value and === comapre value and datatype both
console.log("2" === 2)