// While loop and Do-While

let i = 0
while (i <= 10) {
    //console.log(`Value of i is ${i}`);
    i = i + 2;
}

let myArr = ["ak", "vk", "mk"]
let arr = 0
while (arr < myArr.length) {
    //console.log(`My Array is ${myArr[arr]}`);
    arr = arr + 1
}

let score = 11
do {
    console.log(`Score is ${score}`);
    score++
} while (score <= 10);